package ranges

fun main(args: Array<String>) {

    1..10 // 1,2,3,4..10
    10..1 // 10,9,8,7..1
    1.rangeTo(10) // 1,2,3,4..10
    10.downTo(1) // 10,9,8,7..1
    100 downTo 1
    100 downTo 1 step 5


}